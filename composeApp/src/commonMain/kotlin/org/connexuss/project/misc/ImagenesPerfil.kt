fun obtenerImagenAleatoria(): DrawableResource {
    return mapaImagenesPerfil.values.random()
}

fun obtenerClaveDesdeImagen(drawable: DrawableResource?): String {
    return mapaImagenesPerfil.entries.find { it.value == drawable }?.key ?: "avatar" // fallback
}

fun obtenerImagenDesdeId(id: String?): DrawableResource {
    return mapaImagenesPerfil[id] ?: Res.drawable.avatar
}





